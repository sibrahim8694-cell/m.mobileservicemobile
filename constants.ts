const processCompatibility = (raw: string[], type: 'screen' | 'battery' | 'cover'): CompatibilityItem[] => {
  return raw.map((item, index) => ({
    id: `${type}-${index}`,
    type,
    originalText: item,
    devices: item.split('/').map(d => d.trim())
  }));
};

